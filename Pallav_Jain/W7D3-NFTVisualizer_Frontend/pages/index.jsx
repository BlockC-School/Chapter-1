export default function Home() {
  const [symbol, setSymbol] = useState("");
  const [name, setName] = useState("");
  const [tokenCount, setTokenCount] = useState("");
  const [baseURI, setBaseURI] = useState("");
  const [MarvelCollection, setMarvelCollection] = useState(null);
  const [Address, setAddress] = useState("");

  async function Login() {
    await onboard.walletSelect();
    await onboard.walletCheck();
    const { address } = onboard.getState();
    // setAddress(address);
    console.log("currentState:", address);
  }

  useEffect(() => {
    const EthersProvider = new ethers.providers.JsonRpcProvider(
      process.env.NEXT_PUBLIC_RINKEBY
    );
    const _MarvelCollection = new ethers.Contract(
      process.env.NEXT_PUBLIC_ADDRESS,
      abi,
      EthersProvider
    );

    setAddress("0x045450ff036f5e6fe93a67fdede8bb7426dc2ee8");

    setMarvelCollection(_MarvelCollection);
    // console.log("MarvelCollection:", _MarvelCollection);

    (async () => {
      const _symbol = await _MarvelCollection.symbol();
      setSymbol(_symbol);
      const _name = await _MarvelCollection.name();
      setName(_name);
      const _tokenCount = (await _MarvelCollection.tokenCount()).toNumber();
      setTokenCount(_tokenCount);
      const _baseUri = await _MarvelCollection.baseUri();
      setBaseURI(_baseUri);
    })();
  }, []);

  console.log(MarvelCollection);

  function sendProps() {
    Router.push({
      pathname: "/dashboard",
      query: {
        name,
        baseURI,
        tokenCount,
        Address,
        MarvelCollection,
      },
    });
  }

  // console.log(data);

  return (
    <div>
      <Head>
        <title>NFT Visualizer</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <h1>Welcome to NFT Visualizer</h1>
        <button onClick={Login}>Login</button>
        <h2>NFT Collection Name: {name}</h2>
        <h3>NFT Collection Symbol: {symbol}</h3>
        <h4>NFT Collection BaseURI: {baseURI}</h4>
        <h4>NFT's Minted': {tokenCount}</h4>

        <button onClick={() => sendProps()}>Login</button>
      </main>
    </div>
  );
}
